
package airlinereservationsystem;


public class Price {
    private float econ_price;
    private float business_price;
    private float platinum_price;

    /**
     * @return the platinum_price
     */
    public float getPlatinum_price() {
        return platinum_price;
    }

    /**
     * @param platinum_price the platinum_price to set
     */
    public void setPlatinum_price(float platinum_price) {
        this.platinum_price = platinum_price;
    }

    /**
     * @return the econ_price
     */
    public float getEcon_price() {
        return econ_price;
    }

    /**
     * @param econ_price the econ_price to set
     */
    public void setEcon_price(float econ_price) {
        this.econ_price = econ_price;
    }

    /**
     * @return the business_price
     */
    public float getBusiness_price() {
        return business_price;
    }

    /**
     * @param business_price the business_price to set
     */
    public void setBusiness_price(float business_price) {
        this.business_price = business_price;
    }
    
}
